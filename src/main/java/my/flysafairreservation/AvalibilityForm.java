    private void continueBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continueBtnActionPerformed

    // üîπ Get selected flight and seat
    String flight = (String) flightComboBox.getSelectedItem();
    String seat = (String) seatComboBox.getSelectedItem();

    // üîπ Validation checks
    boolean isFlightCanceled = flight.equals("Flight64");
    boolean isFlightDelayed = flight.equals("FlightMzansi");
    boolean isSeatTaken = !Arrays.asList(
        "5A", "1B", "4C", "2A", "2B", "2C", "3A", "4B", "3C"
    ).contains(seat);

    // üîπ Block if flight is canceled or seat is taken
    if (isFlightCanceled || isSeatTaken) {
        String message = "‚ö†Ô∏è Selected fields are unavailable:\n";
        if (isFlightCanceled) message += "- " + flight + " is canceled.\n";
        if (isSeatTaken) message += "- Seat " + seat + " is occupied.\n";
        message += "Please choose a new flight and seat.";
        JOptionPane.showMessageDialog(null, message, "Selection Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // üîπ Warn if flight is delayed
    if (isFlightDelayed) {
        JOptionPane.showMessageDialog(null,
            "‚è≥ " + flight + " is currently delayed.\nYou may proceed, but expect changes.",
            "Flight Delayed", JOptionPane.WARNING_MESSAGE);
    }

    // üîπ Get remaining flight details
    String departureTime = (String) departureComboBox.getSelectedItem();
    String arrivalTime = (String) arrivalComboBox.getSelectedItem();

    // üîπ Use stored departure/arrival from constructor
    // These were passed from FlightDetailsForm
    // and stored in class-level variables: departure, arrival

    // üîπ Launch TicketForm with all data
    TicketForm ticket = new TicketForm(
        flight, seat,
        departure, arrival, departureTime, arrivalTime, passengerName, email

    );
    ticket.setVisible(true);
    this.dispose();

    }//GEN-LAST:event_continueBtnActionPerformed

